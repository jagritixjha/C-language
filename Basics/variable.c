// Playing with variables.

#include <stdio.h>

int main()

{ // Create variables
  int myNum = 5;
  float myFloatNum = 5.999;
  char myLetter = 'D';

  // Print variables
  printf("%d\n", myNum);
  printf("%f\n", myFloatNum);
  printf("%c\n", myLetter);
  printf("My number is %d and my letter is %c", myNum, myLetter);
  return 0;
}